
public class Arrayejer7 {

	public static void main(String[] args) {
		
		int[] numeros = {5, 8, 3, 12, 7, 6, 9, 4, 1, 10};
		
		 int pares= 0;
		 
		 int impares=0;
		 
		  for (int num : numeros) {
	            if (num % 2 == 0) {
	                pares++;
	            } else {
	                impares++;
	            }
	        }

	        if (pares > impares) {
	            System.out.println("Hay más números pares.");
	        } else if (impares > pares) {
	            System.out.println("Hay más números impares.");
	        } else {
	            System.out.println("Hay la misma cantidad de números pares e impares.");
	
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}}
